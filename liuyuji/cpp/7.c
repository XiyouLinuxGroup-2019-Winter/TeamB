#include<stdio.h>
void main()
{
    int i=1;
    int n=0;
    for(i=1;i<=20;i++){
        n+=i;
    }
    printf("%d\n",n);
    n=0;
    for(i=1;i<=20;i++){
        n=n+i*i;
    }
    printf("%d",n);
}
