#include<stdio.h>
void main()
{
    int i,j,k;
    for(i=1;i<=6;i++){
        for(j=1;j<i;j++){
            k+=j;
        }
        for(j=1;j<=i;j++){
            printf("%c",64+k+j);
        }
        printf("\n");
        k=0;
    }
}
